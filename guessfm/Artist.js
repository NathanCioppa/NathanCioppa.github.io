export class Artist {
    constructor(name, id, gender, type, tags, debutAlbumYear, country, imageUrl, rank) {
        this.name = name
        this.id = id
        this.gender = gender
        this.type = type
        this.tags = tags
        this.debutAlbumYear = debutAlbumYear
        this.country = country
        this.imageUrl = imageUrl
        this.rank = rank
    }
}